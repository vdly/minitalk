#include <unistd.h>

void	bits_sender(char c)
{
	int		i;
	char	bit_holder;

	i = 0;
	while (i < 8)
	{
		holder = (c >> (7 - i)) & 1;
		holder = holder + 48;
		write(1, &holder, 1);
		i++;
	}
	write(1, "/n", 1);
}
